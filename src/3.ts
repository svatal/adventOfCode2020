export function doIt() {
  const maze = input.split(`\n`);
  const width = maze[0].length;
  const results = [1, 3, 5, 7, 0.5].map(
    (slope) =>
      maze
        .map((line, i) => ({ line, x: (i * slope) % width }))
        .map(({ line, x }) => (x % 1 === 0 ? line[x] : undefined))
        .filter((ch) => ch === "#").length
  );
  console.log(
    results[1],
    results.reduce((a, b) => a * b, 1)
  );
}

